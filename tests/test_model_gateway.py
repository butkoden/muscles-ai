def test_openai_adapter_normalizes_text_image_and_embedding_results() -> None:
    class FakeResponses:
        def create(self, **kwargs):
            assert kwargs["model"] == "text-model"
            return SimpleNamespace(output_text="hello from api")

    class FakeImages:
        def generate(self, **kwargs):
            assert kwargs["model"] == "image-model"
            return SimpleNamespace(data=[SimpleNamespace(b64_json="aGVsbG8=")])

    class FakeEmbeddings:
        def create(self, **kwargs):
            assert kwargs["model"] == "embedding-model"
            return SimpleNamespace(
                data=[SimpleNamespace(index=0, embedding=[0.1, 0.2])]
            )

    client = SimpleNamespace(
        responses=FakeResponses(),
        images=FakeImages(),
        embeddings=FakeEmbeddings(),
    )
    model = ModelConfig(
        name="api",
        provider="openai",
        model_id="text-model",
        capabilities=frozenset(
            [
                ModelCapability.TEXT_GENERATE,
                ModelCapability.IMAGE_GENERATE,
                ModelCapability.TEXT_EMBED,
            ]
        ),
    )
    adapter = OpenAIModelAdapter({}, model, client=client)

    text = adapter.invoke(TextGenerationRequest(prompt="hello"), model_id="text-model", options={})
    image = adapter.invoke(
        ImageGenerationRequest(prompt="cat"), model_id="image-model", options={}
    )
    embeddings = adapter.invoke(
        EmbeddingRequest(texts=["hello"]), model_id="embedding-model", options={}
    )

    assert text.text == "hello from api"
    assert image.artifacts[0].data == b"hello"
    assert embeddings.vectors == [[0.1, 0.2]]
