def test_gateway_routes_typed_requests_to_named_models_and_defaults() -> None:
    calls: list[tuple[str, str]] = []

    def generate_text(request, model_id):
        calls.append((request.capability, model_id))
        return TextGenerationResult(text=f"generated by {model_id}")

    def generate_image(request, model_id):
        calls.append((request.capability, model_id))
        return ImageGenerationResult(
            artifacts=[Artifact(media_type="image/png", data=b"png")],
            model=model_id,
        )

    gateway = ModelGateway()
    gateway.register_model(
        "text.local",
        PythonModelAdapter({ModelCapability.TEXT_GENERATE: generate_text}),
        model_id="qwen-local",
        capabilities=[ModelCapability.TEXT_GENERATE],
        default=True,
    )
    gateway.register_model(
        "image.local",
        PythonModelAdapter({ModelCapability.IMAGE_GENERATE: generate_image}),
        model_id="diffusion-local",
        capabilities=[ModelCapability.IMAGE_GENERATE],
    )

    text = gateway.invoke(TextGenerationRequest(prompt="hello"))
    image = gateway.invoke(ImageGenerationRequest(prompt="a cat"), model="image.local")

    assert text.text == "generated by qwen-local"
    assert image.artifacts[0].data == b"png"
    assert calls == [("text.generate", "qwen-local"), ("image.generate", "diffusion-local")]


def test_gateway_rejects_unknown_model_and_unsupported_capability() -> None:
    gateway = ModelGateway()
    gateway.register_model(
        "text",
        PythonModelAdapter(
            {ModelCapability.TEXT_GENERATE: lambda _request, _model: TextGenerationResult(text="ok")}
        ),
        model_id="text",
        capabilities=[ModelCapability.TEXT_GENERATE],
    )

    with pytest.raises(ModelNotFoundError):
        gateway.invoke(TextGenerationRequest(prompt="hello"), model="missing")

    with pytest.raises(ValueError, match="does not support capability"):
        gateway.invoke(EmbeddingRequest(texts=["hello"]), model="text")


